async def send_welcome_message_driver(user_id, message):
    try:
        driver_status = await rq.check_status(user_id)
        if driver_status is None:
            logger.error("–û—à–∏–±–∫–∞ –≤ —Ñ—É–Ω–∫—Ü–∏–∏ send_welcome_message_driver:", exc_info=True)
            return

        if driver_status == 1:
            msg = await message.answer(
                text=um.driver_line_status(driver_status),
                reply_markup=kb.main_button_driver_on_line,
            )
            await rq.set_message(user_id, msg.message_id, msg.text)
        elif driver_status == 2:
            msg = await message.answer(
                text=um.driver_line_status(driver_status),
                reply_markup=kb.main_button_driver_not_on_line,
            )
            await rq.set_message(user_id, msg.message_id, msg.text)
        elif driver_status == 9:
            msg = await message.answer("‚ùóÔ∏è–ï—Å—Ç—å —Ç–µ–∫—É—â–∏–π –∑–∞–∫–∞–∑")
            await rq.set_message(user_id, msg.message_id, msg.text)
        else:
            logger.error(
                f"–ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π —Å—Ç–∞—Ç—É—Å –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_id}: {driver_status}",
                exc_info=True,
            )
            await sup.send_message(message, user_id, um.common_error_message())
    except Exception as e:
        logger.error(
            f"–û—à–∏–±–∫–∞ –≤ —Ñ—É–Ω–∫—Ü–∏–∏ send_welcome_message_driver: {e}", exc_info=True
        )
        await sup.send_message(message, user_id, um.common_error_message())


async def handler_user_state(user_id: int, message: Message, state: FSMContext):
    try:
        user_exists = await sup.origin_check_user(
            user_id, message, state
        )  # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤–æ–∑–≤—Ä–∞—â–∞–µ–º–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ
        if user_exists:  # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ origin_check_user –≤–µ—Ä–Ω—É–ª–∞ True
            data = await state.get_data()
            task = data.get("task")

            user_role = await rq.check_role(user_id)  # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ä–æ–ª—å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
            if user_role is None:
                await state.clear()
                await logger.error(
                    "–û—à–∏–±–∫–∞ –≤ —Ñ—É–Ω–∫—Ü–∏–∏ handler_user_state:", exc_info=True
                )
                await sup.send_message(message, user_id, um.common_error_message())
                return

            if user_role in [2, 3]:
                if task is None:
                    await state.clear()
                    await sup.delete_messages_from_chat(user_id, message)
                await send_welcome_message_driver(user_id, message)
            else:
                if task is None:
                    await state.clear()
                user_name = await rq.get_name(user_id)
                await sup.delete_messages_from_chat(user_id, message)
                await send_welcome_message_client(user_id, user_name, message)
        else:
            logger.info(f"–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å {user_id} –Ω–µ –Ω–∞–π–¥–µ–Ω, –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω –Ω–∞ —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—é.")
            await state.clear()
            return
    except Exception as e:
        await state.clear()
        logger.error(
            f"–û—à–∏–±–∫–∞ –≤ —Ñ—É–Ω–∫—Ü–∏–∏ handler_user_state –¥–ª—è user_id {user_id}: {e}",
            exc_info=True,
        )
        await sup.send_message(message, user_id, um.common_error_message())
